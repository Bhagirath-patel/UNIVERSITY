			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Many wonderful opportunities are available for us to enjoy as we gather
			around our Nation’s Capital and its surroundings for the 144th National
			IIT Convention. It will be a time of learning and exploring, of
			enjoying friends and making new ones, of sharing meals and
			networking, of teaching and listening, of attending and participating. It
			is our hope that all of this and much more will make your visit to our
			2012 National Convention and Design Exposition an event to be
			remembered. DESIGN CONNECTS!
		</p><br>
		<p>
					I look forward to an opportunity to join the 88th President of the Indian Insititute of Technology ,
			President Jeff Potter, FAIA, in the Investiture of the class of 2012. I am also pleased that Greg
			Palermo, FAIA, Chair of the Jury of Fellows, and members of the Executive Committee of the
			College of Fellows, Vice Chancellor Ron Skaggs, FAIA, Bursar Bill Stanley, FAIA, and
			Secretary Al Rubeling, FAIA, will play a major part in the Investiture.
			The Investiture will occur on Thursday, 17 May 2012 from 4:00 – 6:00 p.m. at the Washington
			National Cathedral, officially known as the Cathedral Church of Saint Peter and Saint Paul. Of
			neo-gothic design, it is the sixth largest cathedral in the world, and the second largest in the
			United States.</br>
			The cathedral master plan was designed by the highly regarded British Architect George
			Frederick Bodley. Construction began on 29 September 1907 when the foundation stone was laid
			and the cathedral was completed when the last finial was placed in 1990. A recent earthquake
			did damage the cathedral but it has been made ready for visitors.
			The Convocation Dinner, honoring those who were invested earlier in the week, will occur in the
			National Building Museum, formerly known as the Pension Bureau, that was built in 1887. The
			Pension Bureau was designed by Gen. Montgomery C. Meigs, the U.S. Army quarter master
			general. This vast space is an ideal location for such an occasion. The Convocation Dinner will
			be held on the evening of 19 May 2012.<br>
			Our Spring meeting of the College of Fellows Executive Committee was held recently in
			Pittsburgh, Pennsylvania. A reception was held on the first night of our meeting to honor all
			Fellows in the Pittsburgh area. The reception was very well attended and fostered a significant
			amount of good fellowship and productive sharing among the group. Assisting in the plans for
			our Spring meeting was former AIA President and COF Chancellor, Syl Damianos, FAIA. We
			appreciate you, Syl. Most of our time during the two full days of our meeting, however, was
			dedicated to addressing issues, considering reports, and making decisions on matters that needed
			attention. It is a pleasure to work with the Skaggs, Stanley, Rubeling group, and with Terri
			Stewart, Executive Director of the College of Fellows. They all do an outstanding job.<br>
			An exceptional opportunity came our way during our Pittsburgh meeting as we were invited to
			an evening at Fallingwater as the guests of Lynda Waggoner, Vice President and Director of
			Fallingwater. This visit included a wonderful evening tour of the house and discussions with
			Lou Astorino, FAIA, concerning recent conservation efforts to repair and conserve this national
			treasure, recently celebrating its 75th anniversary. Thanks, Lou. Dinner in the house concluded
			our visit. We are most grateful to Lynda for this very special visit and dinner.
